begin
	function ffill!(vec)
	    last_value = missing
	    for i in eachindex(vec)
	        if ismissing(vec[i])
	            vec[i] = last_value
	        else
	            last_value = vec[i]
	        end
	    end
	end
	
	function load_cpCST_csv(filepath)
	    df = CSV.read(filepath, DataFrame)
	    ffill!(df.user_pos)
	    ffill!(df.stim_pos)
	    df[!, :user_pos] .= df.user_pos .* -1
	    df[!, :time_secs] .= df.flip_time .- df.flip_time[1]
	    return df
	end
	
	function get_dtw_vals(df, radius=120, thresh=1e-12)
	    a = skipmissing(df.stim_pos) |> collect
	    b = skipmissing(df.user_pos) |> collect
	    dist = SqEuclidean(thresh)
	    cost, i1, i2 = fastdtw(a, b, dist, radius)
	    return DataFrame(user=i1, stim=i2)
	end
	
	function get_row(dat, tgt_val)
	    row = filter(r -> r.user == tgt_val, dat)
	    row[!, :user_ts] .= row.user .* (1/60)
	    row[!, :stim_ts] .= row.stim .* (1/60)
	    return row
	end
	
	function get_point_rt(dat)
	    return mean(dat.stim_ts) - dat.user_ts[1]
	end
	
	function compute_irt(df, radius)
	    dat = get_dtw_vals(df, radius)
	    return [get_point_rt(get_row(dat, u)) for u in eachindex(df.user_pos)]
	end
	
	function adjust_radius(df, filepath, destination_folder; max_attempts=7, initial_radius=120, step=10, min_radius=50)
	    radius = initial_radius
	    attempt = 1
	    while attempt ≤ max_attempts && radius ≥ min_radius
	        irt = compute_irt(df, radius)
	        mean_irt = mean(irt)
	
	        # Debugging output for radius and IRT values
	        display("Thread B - $(basename(filepath)) | Attempt: $attempt | Radius: $radius, Mean IRT: $mean_irt")
	
	        if 0 ≤ mean_irt ≤ 5
	            df[!, :irt] = irt
	            dest_file = joinpath(destination_folder, basename(filepath))
	            CSV.write(dest_file, df)
	
	            display("Thread B - $(basename(filepath)) | IRT within range, final radius: $radius ✅")
	            return
	        end
	
	        # Update the radius
	        radius -= step
	        attempt += 1
	    end
	
	    display("Thread B - $(basename(filepath)) | Max radius reductions reached or IRT not in range. Saving last result.")
	    df[!, :irt] = compute_irt(df, radius)
	    dest_file = joinpath(destination_folder, basename(filepath))
	    CSV.write(dest_file, df)
	end
	
	function process_files(source_folder, destination_folder; participants=[])
	    csv_files = glob("*.csv", source_folder)
	    if !isempty(participants)
	        csv_files = [file for file in csv_files if any(occursin(participant, file) for participant in participants)]
	    end
	
	    @sync for file in csv_files
	        @spawn begin
	            display("Thread A - Processing: $(basename(file))")  # Real-time display
	
	            df = load_cpCST_csv(file)
	            irt = compute_irt(df, 120)
	            mean_irt = mean(irt)
	
	            display("Thread A - Mean IRT for $(basename(file)): $mean_irt")
	
	            if 0 ≤ mean_irt ≤ 5
	                df[!, :irt] = irt
	                dest_file = joinpath(destination_folder, basename(file))
	                CSV.write(dest_file, df)
	
	                display("Thread A - Completed: $(basename(file)) ✅")
	            else
	                display("Thread A - $(basename(file)) moved to Thread B for dynamic radius adjustment.")
	                @spawn adjust_radius(df, file, destination_folder)
	            end
	        end
	    end
	end
end


